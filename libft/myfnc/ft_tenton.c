#include "libft.h"

int		ft_tenton(int n)
{
	int res;

	res = 0;
	if (n < 10 && n > 0)
	{
		if (n == 0)
			res = 1;
		while (n > 0)
		{
			res = res * 10;
			n--;
		}
	}
	return (res);
}
