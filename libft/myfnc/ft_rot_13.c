#include "libft.h"

void	ft_rot_13(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			ft_putchar(((str[i] - 91) + 13) % 25 + 91);
		}
		else if (str[i] >= 'A' && str[i] <= 'Z')
		{
			ft_putchar(((str[i] - 64) + 13) % 25 + 64);
		}
		else
		{
			ft_putchar(str[i]);
		}
		i++;
	}
}
